weather = "snowy"
time_of_day = "night"

if weather == "sunny":
    print("You can play with your car toy.")
elif weather == "rainy":
    print("You can play with your boat toy.")
elif weather == "snowy" and time_of_day == "night":
    print("You can play with your teddy bear toy.")
else:
    print("You can play with your snowman toy.")

