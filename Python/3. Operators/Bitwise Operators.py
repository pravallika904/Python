#Bitwise AND (&) Operator
result_and = 6 & 3
print(result_and)


#Bitwise OR (|) Operator
result_or = 6 | 3
print(result_or)


#Bitwise XOR (^) Operator
result_xor = 6 ^ 3
print(result_xor)


#Bitwise NOT (~) Operator
result_not = ~6
print(result_and)


#Bitwise Left Shift (<<) Operator
result_left_shift = 2 << 2
print(result_left_shift)


#Bitwise Right Shift (>>) Operator
result_right_shift = 32 >> 2
print(result_right_shift)