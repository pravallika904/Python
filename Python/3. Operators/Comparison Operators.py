#Equal to (==)
x = 5
y = 10
result_equal = x == y
print(result_equal)

#Not Equal to (!=)
a = 7
b = 7
result_not_equal = a != b
print(result_not_equal)

#Greater than (>)
num1 = 15
num2 = 10
result_greater_than = num1 > num2
print(result_greater_than)

#Less than(<)
p = 4
q = 8
result_less_than = p < q
print(result_less_than)

#Greater than or Equal to (>=)
m = 5
n = 5
result_greater_equal = m >= n
print(result_greater_equal)

#Less than or Equal to (<=)
r = 10
s = 15
result_less_equal = r <= s
print(result_less_equal)
